/**
 * Deca Identifier
 *
 * @author gl49
 * @date 01/01/2021
 */
public class Identifier extends AbstractIdentifier {

    
    @Override
    protected void checkDecoration() {
        if (getDefinition() == null) {
            throw new DecacInternalError("Identifier " + this.getName() + " has no attached Definition");
        }
    }

    @Override
    public Definition getDefinition() {
        return definition;
    }

    /**
     * Like {@link #getDefinition()}, but works only if the definition is a
     * ClassDefinition.
     * 
     * This method essentially performs a cast, but throws an explicit exception
     * when the cast fails.
     * 
     * @throws DecacInternalError
     *             if the definition is not a class definition.
     */
    @Override
    public ClassDefinition getClassDefinition() {
        try {
            return (ClassDefinition) definition;
        } catch (ClassCastException e) {
            throw new DecacInternalError(
                    "Identifier "
                            + getName()
                            + " is not a class identifier, you can't call getClassDefinition on it");
        }
    }

    /**
     * Like {@link #getDefinition()}, but works only if the definition is a
     * MethodDefinition.
     * 
     * This method essentially performs a cast, but throws an explicit exception
     * when the cast fails.
     * 
     * @throws DecacInternalError
     *             if the definition is not a method definition.
     */
    @Override
    public MethodDefinition getMethodDefinition() {
        try {
            return (MethodDefinition) definition;
        } catch (ClassCastException e) {
            throw new DecacInternalError(
                    "Identifier "
                            + getName()
                            + " is not a method identifier, you can't call getMethodDefinition on it");
        }
    }

    /**
     * Like {@link #getDefinition()}, but works only if the definition is a
     * FieldDefinition.
     * 
     * This method essentially performs a cast, but throws an explicit exception
     * when the cast fails.
     * 
     * @throws DecacInternalError
     *             if the definition is not a field definition.
     */
    @Override
    public FieldDefinition getFieldDefinition() {
        try {
            return (FieldDefinition) definition;
        } catch (ClassCastException e) {
            throw new DecacInternalError(
                    "Identifier "
                            + getName()
                            + " is not a field identifier, you can't call getFieldDefinition on it");
        }
    }

    /**
     * Like {@link #getDefinition()}, but works only if the definition is a
     * VariableDefinition.
     * 
     * This method essentially performs a cast, but throws an explicit exception
     * when the cast fails.
     * 
     * @throws DecacInternalError
     *             if the definition is not a field definition.
     */
    @Override
    public VariableDefinition getVariableDefinition() {
        try {
            return (VariableDefinition) definition;
        } catch (ClassCastException e) {
            throw new DecacInternalError(
                    "Identifier "
                            + getName()
                            + " is not a variable identifier, you can't call getVariableDefinition on it");
        }
    }

    /**
     * Like {@link #getDefinition()}, but works only if the definition is a ExpDefinition.
     * 
     * This method essentially performs a cast, but throws an explicit exception
     * when the cast fails.
     * 
     * @throws DecacInternalError
     *             if the definition is not a field definition.
     */
    @Override
    public ExpDefinition getExpDefinition() {
        try {
            return (ExpDefinition) definition;
        } catch (ClassCastException e) {
            throw new DecacInternalError(
                    "Identifier "
                            + getName()
                            + " is not a Exp identifier, you can't call getExpDefinition on it");
        }
    }

    @Override
    public void setDefinition(Definition definition) {
        this.definition = definition;
    }

    @Override
    public Symbol getName() {
        return name;
    }

    private Symbol name;

    public Identifier(Symbol name) {
        Validate.notNull(name);
        this.name = name;
    }

    @Override
    public Type verifyExpr(DecacCompiler compiler, EnvironmentExp localEnv,
            ClassDefinition currentClass) throws ContextualError {
//        throw new UnsupportedOperationException("not yet implemented");
    	if (localEnv.get(name) == null) {
            throw new ContextualError("Identifier must be declared ! (0.1)", this.getLocation());
        }
        this.setDefinition(localEnv.get(name));
        this.setType(localEnv.get(name).getType());

        return localEnv.get(name).getType();
        
    }

    /**
     * Implements non-terminal "type" of [SyntaxeContextuelle] in the 3 passes
     * @param compiler contains "env_types" attribute
     */
    @Override
    public Type verifyType(DecacCompiler compiler) throws ContextualError {
//        throw new UnsupportedOperationException("not yet implemented");
    	if (!compiler.getEnvironementType().containsKey(name)) {
            throw new ContextualError("Cannot find type ! (0.2)", this.getLocation());
        }
        setDefinition(compiler.getEnvironementType().get(name));
        this.setType(compiler.getEnvironementType().get(name).getType());

        return compiler.getEnvironementType().get(name).getType();
    }
    
    
    private Definition definition;

    @Override
    public void codeGenInst(DecacCompiler compiler){
    	GPRegister nouveauReg = Register.getR(GestionMemoire.nouveauRegistre(compiler));
    	//GPRegister dernierReg = Register.getR(GestionMemoire.getPositionDernierRegistreUtilise(compiler, false));
        if (definition.isField()) {
        	 compiler.addInstruction(new LOAD(new RegisterOffset(-2, Register.LB), nouveauReg));
             compiler.addInstruction(new LOAD(new RegisterOffset(this.getFieldDefinition().getIndex(), nouveauReg), nouveauReg));
        }else if (definition.isExpression()){
            compiler.addInstruction(new LOAD(getExpDefinition().getOperand(), nouveauReg));
        	//GestionMemoire.incrementeVarPosition();
        	//compiler.addInstruction(new LOAD(new RegisterOffset(GestionMemoire.getVariablePosition()-1, Register.GB), nouveauReg));

        }else if (definition.isClass()) {
            compiler.addInstruction(new LOAD(getClassDefinition().getOperand(),nouveauReg));
        }

    }
    @Override
    protected void iterChildren(TreeFunction f) {
        // leaf node => nothing to do
    }

    @Override
    protected void prettyPrintChildren(PrintStream s, String prefix) {
        // leaf node => nothing to do
    }

    @Override
    public void decompile(IndentPrintStream s) {
        s.print(name.toString());
    }

    @Override
    String prettyPrintNode() {
        return "Identifier (" + getName() + ")";
    }

    @Override
    protected void prettyPrintType(PrintStream s, String prefix) {
        Definition d = getDefinition();
        if (d != null) {
            s.print(prefix);
            s.print("definition: ");
            s.print(d);
            s.println();
        }
    }

	@Override
	public void codeGenPrint(DecacCompiler compiler) {
		
		compiler.addInstruction(new LOAD(getExpDefinition().getOperand(), Register.getR(1)));
    	//compiler.addInstruction(new LOAD(new RegisterOffset(GestionMemoire.getVariablePosition(), Register.GB), Register.R1));

        if (getExpDefinition().getType().isFloat()) {
            if (super.isHexaDecimal())
                compiler.addInstruction(new WFLOATX());
            else
                compiler.addInstruction(new WFLOAT());
        }
        
        else if (getExpDefinition().getType().isInt())
            compiler.addInstruction(new WINT());
        
        else if (getExpDefinition().getType().isBoolean())
            compiler.addInstruction(new WINT());
        
		
	}
    


}